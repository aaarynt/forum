const themes = [orangeTheme, twitterTheme, solarTheme, brutalTheme, spotifyTheme, artDecoTheme]

export function getThemePresets() {
  return themes
}

export function getThemePreset(id: string | null | undefined) {
  return themes.find((theme) => theme.id === id) ?? themes[0]
}

export function getStoredThemePreset() {
  if (typeof window === 'undefined') return themes[0]
  return getThemePreset(window.localStorage.getItem(STORAGE_KEY))
}

export function saveThemePreset(id: string) {
  if (typeof window === 'undefined') return
  window.localStorage.setItem(STORAGE_KEY, id)
}

export function applyThemePreset(preset: ThemePreset, mode: ThemeMode) {
  if (typeof document === 'undefined') return
  const root = document.documentElement
  const vars = mode === 'dark' ? preset.dark : preset.light
  Object.entries(vars).forEach(([key, value]) => {
    root.style.setProperty(key, value)
  })
  root.dataset.themePreset = preset.id
}
